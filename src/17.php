<?php

// Example 1: Square root function
echo sqrt(4); // Output: 2.0

// Example 2: Exponential function with base e (base of natural logarithm)
echo exp(3); // Output: 21.0337659875966

// Example 3: Logarithmic function
echo log(5, 3); // Output: 1.4649516003116132

// Example 4: Absolute value function
echo abs(-5); // Output: 5

// Example 5: Ceiling function
echo ceil(3.75); // Output: 4

// Example 6: Floor function
echo floor(-3.75); // Output: -4

// Example 7: Modulo operation
echo mod(10, 3); // Output: 1

// Example 8: Power function (base a and exponent b)
echo pow(2, 3); // Output: 8

// Example 9: Inverse cosine function
echo cosh(0.5); // Output: 1.648721270700128

// Example 10: Tangent function
echo tan(3.14); // Output: -1.224744871398105e-16 (close to 0)

?>
